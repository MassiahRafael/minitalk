#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-2147483648", 11);
		}
		else
		{
			write (fd, "-", 1);
			n = -n;
		}
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	if (n >= 0)
	{
		n = n % 10;
		n = n + 48;
		write(fd, &n, 1);
	}
}
